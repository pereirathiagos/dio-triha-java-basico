public class ExercicioTiposVariaveis {

    public static void main(String[] args) {
        // Declaração e atribuição de variáveis
        int numeroInteiro = 10;
        byte numeroByte = 100;
        short numeroShort = 1000;
        long numeroLong = 1000000L; 
        float numeroFloat = 3.14F; 
        double numeroDouble = 3.14159265359;
        boolean booleano = true;
        char caractere = 'A';

        // Impressão dos valores das variáveis
        System.out.println("Valor do número inteiro: " + numeroInteiro);
        System.out.println("Valor do número byte: " + numeroByte);
        System.out.println("Valor do número short: " + numeroShort);
        System.out.println("Valor do número long: " + numeroLong);
        System.out.println("Valor do número float: " + numeroFloat);
        System.out.println("Valor do número double: " + numeroDouble);
        System.out.println("Valor do booleano: " + booleano);
        System.out.println("Valor do caractere: " + caractere);
    }
}
